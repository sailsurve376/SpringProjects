package com.tcs.spring_getter_setter_injector_collection;

public class ColorType {
	String color;

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	@Override
	public String toString() {
		return "ColorType [color=" + color + "]";
	}
	
}
